/**
 * learn to loop a process using for loop
 */

#include <iostream>

int main()
{
    /**
     * what this for loop will do??
     * this will store integer x as 1
     * it will increment it by 1 till it reaches 20 :)
     */

    for (int a =  1; a < 20; a = a + 1) {
        std::cout << "value of a: " << a << std::endl;
    }

    return 0;
}